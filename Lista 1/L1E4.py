#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
soma = 0
for x in range(1,5):
  print("INFORME SUA NOTA NO %.0fº BIMESTRE: "% x)
  nota = float(input())
  soma = nota + soma
media = (soma/4)
print("====================================================")
print("SUA MÉDIA É IGUAL A %.2f" % round(media,2))
print("====================================================")
